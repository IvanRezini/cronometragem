    private void jButtonSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSalvarMouseClicked
        if (validarCampos()) {
            Produto pro = new Produto();
            String cod = labelSetCodigo.getText();
            int codi = Integer.parseInt(cod);

            pro.setNomeProduto(campoNome.getText());
            pro.setDescProduto(campoDescricao.getText());
            pro.setCodProduto(codi);
            try {
                ProdutoDao proDao = new ProdutoDao();
                proDao.alterar(pro);
                JOptionPane.showMessageDialog(null, "Produto alterado com susesso");
                this.listar = 1; //apenas permite a listagem
                this.popularTabela();
                this.add(panelListar, "panelListar");
                this.cl.show(this, "panelListar");

            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(PanelCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Falha na atualização ");
            }
        }
    }

    private boolean validarCampos() {
        String nome = campoNome.getText();
        if (nome.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe um nome");
            return false;
        }
        String desc = campoDescricao.getText();
        if (desc.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe uma Descrição");
            return false;
        } else {
            return true;
        }


    }//GEN-LAST:event_jButtonSalvarMouseClicked

