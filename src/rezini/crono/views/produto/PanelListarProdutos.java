    private void popularTabela() throws SQLException {

        ProdutoDao pro = new ProdutoDao();
        List <Produto> listaProdutos;
        try {
            listaProdutos = pro.listaProdutos();

            DefaultTableModel model = (DefaultTableModel) tableProduto.getModel();
            List<Object> lista = new ArrayList<Object>();

            for (int i = 0; i < listaProdutos.size(); i++) {
                Produto p = listaProdutos.get(i);
                lista.add(new Object[]{p.getcodProduto(), p.getNomeProduto(), p.getDescProduto(), p.getCreateProduto()});
            }
            for (int idx = 0; idx < lista.size(); idx++) {
                model.addRow((Object[]) lista.get(idx));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Obter dados do Banco de Dados!");
            //Logger.getLogger(PanelListarProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

