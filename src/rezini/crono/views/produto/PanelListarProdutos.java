    private void tableProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdutoMouseClicked
        int linha = tableProduto.getSelectedRow();
        String codigo = tableProduto.getValueAt(linha, 0).toString();
        int cod = Integer.parseInt(codigo);
        if (this.listar == 2) {
            try {
                this.setProduto(cod);
                this.add(panelEditar, "panelEdicao");
                this.cl.show(this, "panelEdicao");
                this.limparTabela();
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(PanelListarProdutos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tableProdutoMouseClicked
    private void setProduto(int a) throws SQLException {
        ProdutoDao proD = new ProdutoDao();

        Produto pro = proD.getProduto(a);
        String cod = "" + a;
        labelSetCodigo.setText(cod);
        campoNome.setText(pro.getNomeProduto());
        campoDescricao.setText(pro.getDescProduto());
    }
